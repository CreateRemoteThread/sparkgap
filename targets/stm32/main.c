void stm_gets(char *p)
{
  char r = 0;
  int wHead = 0;
  while(r != 0x0d && r != 0x0a)
  {
    HAL_UART_Receive(&huart1,&r,1,HAL_MAX_DELAY);
    p[wHead++] = r;
  }
  p[wHead] = 0;
  return;
}

